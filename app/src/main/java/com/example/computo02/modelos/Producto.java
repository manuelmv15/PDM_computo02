package com.example.computo02.modelos;

public class Producto {
    public int img;
    public String nombre;
    public double precio;
    public int stock;

    public Producto(int img, String nombre, double precio, int stock) {
        this.img = img;
        this.nombre = nombre;
        this.precio = precio;
        this.stock = stock;
    }
}
